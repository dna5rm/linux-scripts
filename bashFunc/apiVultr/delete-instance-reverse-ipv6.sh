## delete-instance-reverse-ipv6 # Delete Instance Reverse IPv6
# /instances/{instance-id}/ipv6/reverse/{ipv6}

function delete-instance-reverse-ipv6 ()
{
    # Verify function requirements
    for req in curl; do
        type  >/dev/null 2>&1 || {
            echo >&2 "$(basename "${0}" 2> /dev/null):${FUNCNAME[0]} - ${req} is not installed. Aborting."
            exit 1
        }
    done

    if [[ -z "${VULTR_API_URI}" ]] || [[ -z "${VULTR_API_KEY}" ]] || [[ -z "${2}" ]]; then
	cat <<-EOF
	$(basename "${0}" 2> /dev/null):${FUNCNAME[0]} - Delete the reverse IPv6 for an Instance.
	Ref: /instances/{instance-id}/ipv6/reverse/{ipv6}
	---
	API Base URI: \${VULTR_API_URI} (${VULTR_API_URI:-required})
	Authorization Key: \${VULTR_API_KEY} (${VULTR_API_KEY:-required})

	[3mCode  Description[m
	204   No Content
	400   Bad Request
	401   Unauthorized
	403   Forbidden
	
	EOF
    else
        curl --silent --location --request DELETE --url "${VULTR_API_URI}/instances/${1}/ipv6/reverse/${2}" \
          --header "Authorization: Bearer ${VULTR_API_KEY}" \
          --header "Content-Type: application/json" \
          --data "${3:-{\}}"
    fi
}
